def IsLeapYear(Y):
    """
    Определяет, является ли год Y високосным.
    
    Високосный год:
    - делится на 4 И
    - (не делится на 100) ИЛИ (делится на 400)
    """
    return (Y % 4 == 0) and (Y % 100 != 0 or Y % 400 == 0)

# Примеры использования
print(IsLeapYear(2024))  # True (делится на 4, не делится на 100)
print(IsLeapYear(1900))  # False (делится на 4 и на 100, но не на 400)
print(IsLeapYear(2000))  # True (делится на 4, на 100 и на 400)
print(IsLeapYear(2023))  # False (не делится на 4)
